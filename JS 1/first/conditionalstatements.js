// if and else statements
let num = 20
if(num == 20){
    console.log("number is matched with 20")
}
else if(num == 13){
    console.log("number is equal to 13")
}
else if(num > 20){
    console.log("number is greater than 20")
}
else{
    console.log("number is not matching")
}

